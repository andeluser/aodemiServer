package cardDataObject;

public class Shield {

	private String id;
	private String life;
	private String skillName;
	private String skillSP;
	private String skillDetail;
	private String skillSelect;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLife() {
		return life;
	}
	public void setLife(String life) {
		this.life = life;
	}
	public String getSkillName() {
		return skillName;
	}
	public void setSkillName(String skillName) {
		this.skillName = skillName;
	}
	public String getSkillSP() {
		return skillSP;
	}
	public void setSkillSP(String skillSP) {
		this.skillSP = skillSP;
	}
	public String getSkillDetail() {
		return skillDetail;
	}
	public void setSkillDetail(String skillDetail) {
		this.skillDetail = skillDetail;
	}
	public String getSkillSelect() {
		return skillSelect;
	}
	public void setSkillSelect(String skillSelect) {
		this.skillSelect = skillSelect;
	}

}
